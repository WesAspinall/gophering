// Concurrency - A design pattern "concurrent code"

// Parallelism - code that runs simultaneously executing on different threads

// "Concurrent code has the ability to run in parallel,
// given that the CPU has more than one core."

// But when people hear the word concurrency they often think of parallelism,
// a related but quite distinct concept.
// In programming, concurrency is the composition of independently executing processes,
// while parallelism is the simultaneous execution of (possibly related) computations.
// Concurrency is about dealing with lots of things at once.
// Parallelism is about doing lots of things at once.